<p>顺着这句话，还有更省事的做法。经济学里有个词叫 cheap talk，直译是零成本的空口白话，它证明了一件很有用的事：如果说话的人和听话的人利益一致，说真话根本不用花钱，空口白话就够可信；反过来，双方利益冲突越大，才越需要昂贵的信号去把谎话压住。一句话，维持诚实要花多少成本，跟利益冲突的大小成正比。</p>
